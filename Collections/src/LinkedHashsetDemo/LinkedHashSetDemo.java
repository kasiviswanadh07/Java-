package LinkedHashsetDemo;

import java.util.LinkedHashSet;

public class LinkedHashSetDemo {

	public static void main(String[] args) {

		LinkedHashSet <Integer> set=new LinkedHashSet<>();
		set.add(33);
		set.add(53);
		set.add(22);
		set.add(10);
		set.add(44);
		System.out.println(set);
	}

}
